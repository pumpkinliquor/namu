package fbcms.admin.hpm.cln.service.Impl;

public class CodeDetailVO {
	
	private String CODE_ID;
	private String CODE;
	private String CODE_NM;
	private String CODE_CN;

	/**
	 * @return the cODE_ID
	 */
	public String getCODE_ID() {
		return CODE_ID;
	}

	/**
	 * @param cODE_ID the cODE_ID to set
	 */
	public void setCODE_ID(String cODE_ID) {
		CODE_ID = cODE_ID;
	}

	/**
	 * @return the cODE
	 */
	public String getCODE() {
		return CODE;
	}

	/**
	 * @param cODE the cODE to set
	 */
	public void setCODE(String cODE) {
		CODE = cODE;
	}

	/**
	 * @return the cODE_NM
	 */
	public String getCODE_NM() {
		return CODE_NM;
	}

	/**
	 * @param cODE_NM the cODE_NM to set
	 */
	public void setCODE_NM(String cODE_NM) {
		CODE_NM = cODE_NM;
	}

	/**
	 * @return the cODE_CN
	 */
	public String getCODE_CN() {
		return CODE_CN;
	}

	/**
	 * @param cODE_CN the cODE_CN to set
	 */
	public void setCODE_CN(String cODE_CN) {
		CODE_CN = cODE_CN;
	}

}
